"use server";
import { connectToDB } from "@/lib/mongoose";
import Product from "@/lib/product-model";

export async function getProductById(productId: string) {
  try {
    connectToDB();
    const product = await Product.findOne({ _id: productId });
    if (!product) return null;
    return product;
  } catch (error) {
    console.log(error);
  }
}
